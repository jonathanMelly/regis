// buildNatureSection parses internal/cue/*.go for // doc:nature blocks.

func buildNatureSection(cueDir string) string {

	cueFiles, err := filepath.Glob(filepath.Join(cueDir, "*.go"))

	if err != nil || len(cueFiles) == 0 {

		return ""

	}



	var entries []natureEntry



	for _, path := range cueFiles {

		data, err := os.ReadFile(path)

		if err != nil {

			continue

		}

		lines := strings.Split(string(data), "\n")



		var natureName string

		var docLines []string



		for _, line := range lines {

			line = strings.TrimRight(line, "\r")

			stripped := strings.TrimPrefix(line, "// ")

			stripped = strings.TrimPrefix(stripped, "//")



			if strings.HasPrefix(stripped, "doc:nature ") {

				natureName = strings.TrimPrefix(stripped, "doc:nature ")

				natureName = strings.TrimSpace(natureName)

				docLines = nil

				continue

			}



			if natureName != "" {

				if strings.HasPrefix(line, "//") {

					docLines = append(docLines, strings.TrimSpace(stripped))

				} else {

					// End of comment block

					entries = append(entries, natureEntry{name: natureName, lines: docLines})

					natureName = ""

					docLines = nil

				}

			}

		}

		// flush if file ended while in a block

		if natureName != "" {

			entries = append(entries, natureEntry{name: natureName, lines: docLines})

		}

	}



	// Sort by nature name for stable output

	sort.Slice(entries, func(i, j int) bool {

		return entries[i].name < entries[j].name

	})



	var buf bytes.Buffer

	for _, e := range entries {

		buf.WriteString(fmt.Sprintf("### %s\n\n", e.name))

		for _, l := range e.lines {

			if l != "" {

				buf.WriteString(l + "\n")

			}

		}

		buf.WriteString("\n")

	}

	buf.WriteString("---\n\n")

	return buf.String()

}



// buildInterpolSection parses internal/config/interpolate.go for the // doc:interpolation block.

func buildInterpolSection(interpolFile string) (string, error) {

	data, err := os.ReadFile(interpolFile)

	if err != nil {

		return "", fmt.Errorf("regis-docgen: read %s: %w", interpolFile, err)

	}



	lines := strings.Split(string(data), "\n")

	var docLines []string

	inBlock := false



	for _, line := range lines {

		line = strings.TrimRight(line, "\r")

		stripped := strings.TrimPrefix(line, "// ")

		stripped = strings.TrimPrefix(stripped, "//")



		if strings.TrimSpace(stripped) == "doc:interpolation" {

			inBlock = true

			continue

		}



		if inBlock {

			if strings.HasPrefix(line, "//") {

				docLines = append(docLines, strings.TrimSpace(stripped))

			} else {

				break

			}

		}

	}



	var buf bytes.Buffer

	for _, l := range docLines {

		buf.WriteString(l + "\n")

	}

	buf.WriteString("\n---\n\n")

	return buf.String(), nil

}



// cobraCmd holds a parsed cobra.Command Use + Short pair.

type cobraCmd struct {

	use   string

	short string

}



// buildCLISection parses cmd/regis/cmd/*.go for cobra.Command Use+Short fields.

func buildCLISection(cobraDir string) string {

	goFiles, err := filepath.Glob(filepath.Join(cobraDir, "*.go"))

	if err != nil || len(goFiles) == 0 {

		return ""

	}



	fset := token.NewFileSet()

	var cmds []cobraCmd



	for _, path := range goFiles {

		// Skip test files

		if strings.HasSuffix(path, "_test.go") {

			continue

		}

		f, err := parser.ParseFile(fset, path, nil, 0)

		if err != nil {

			continue

		}

		found := extractCobraCommands(f)

		cmds = append(cmds, found...)

	}



	// Deduplicate by Use

	seen := make(map[string]bool)

	var unique []cobraCmd

	for _, c := range cmds {

		if !seen[c.use] {

			seen[c.use] = true

			unique = append(unique, c)

		}

	}



	// Filter: skip subcommand verbs (service subcommands like "start <name>", state subcommands)

	// and skip the root command "regis" itself.

	skipVerbs := map[string]bool{

		"start": true, "stop": true, "restart": true, "reload": true,

		"enable": true, "disable": true, "logs": true,

		"list": true, "current": true, "compensate": true,

		"regis": true, // root command

	}

	var topLevel []cobraCmd

	for _, c := range unique {

		firstWord := strings.Fields(c.use)[0]

		if skipVerbs[firstWord] {

			continue

		}

		topLevel = append(topLevel, c)

	}



	// Sort by Use for stable output

	sort.Slice(topLevel, func(i, j int) bool {

		return topLevel[i].use < topLevel[j].use

	})



	var buf bytes.Buffer



	buf.WriteString("### Global flags\n\n")

	buf.WriteString("| flag | short | description |\n")

	buf.WriteString("|------|-------|-------------|\n")

	buf.WriteString("| `--file` | `-f` | config file (default: regis.yml) |\n")

	buf.WriteString("| `--target` | | target selector: name, comma-list, `all`, or glob |\n")

	buf.WriteString("| `--run-without-check` | | skip Phase 1 rdiff and deploy directly (CI/automation) |\n")
	buf.WriteString("| `--allow-dirty` | | allow rdiff and deploy with uncommitted changes |\n")
	buf.WriteString("| `--no-git` | | allow rdiff and deploy without a git repository |\n")

	buf.WriteString("| `--verbose` | `-v` | show unchanged cues and full command output |\n")

	buf.WriteString("| `--quiet` | `-q` | errors and summary only |\n")

	buf.WriteString("| `--plain` | | force plain output (level 1) |\n")

	buf.WriteString("| `--nature` | `-N` | filter cues by nature: binary,config,secret,action,generate,render |\n")

	buf.WriteString("\n---\n\n")



	buf.WriteString("### Commands\n\n")

	buf.WriteString("| command | description |\n")

	buf.WriteString("|---------|-------------|\n")

	for _, c := range topLevel {

		buf.WriteString(fmt.Sprintf("| `regis %s` | %s |\n", c.use, c.short))

	}

	buf.WriteString("\n---\n\n")



	return buf.String()

}



// extractCobraCommands walks the AST and returns Use+Short from cobra.Command literals.

func extractCobraCommands(f *ast.File) []cobraCmd {

	var results []cobraCmd



	ast.Inspect(f, func(n ast.Node) bool {

		lit, ok := n.(*ast.CompositeLit)

		if !ok {

			return true

		}



		// Check if this is a cobra.Command literal

		sel, ok := lit.Type.(*ast.SelectorExpr)

		if !ok {

			return true

		}

		pkg, ok := sel.X.(*ast.Ident)

		if !ok || pkg.Name != "cobra" || sel.Sel.Name != "Command" {

			return true

		}



		var use, short string

		for _, elt := range lit.Elts {

			kv, ok := elt.(*ast.KeyValueExpr)

			if !ok {

				continue

			}

			key, ok := kv.Key.(*ast.Ident)

			if !ok {

				continue

			}

			val, ok := kv.Value.(*ast.BasicLit)

			if !ok || val.Kind != token.STRING {

				continue

			}

			// val.Value is a Go string literal; strip surrounding quotes/backticks

			strVal := val.Value

			if len(strVal) >= 2 {

				if (strVal[0] == '"' && strVal[len(strVal)-1] == '"') ||

					(strVal[0] == '`' && strVal[len(strVal)-1] == '`') {

					strVal = strVal[1 : len(strVal)-1]

				}

			}

			switch key.Name {

			case "Use":

				use = strVal

			case "Short":

				short = strVal

			}

		}



		if use != "" && short != "" {

			results = append(results, cobraCmd{use: use, short: short})

		}



		return true

	})



	return results

}



// extractStructs returns a map of struct name -> ordered []fieldInfo,

// only for fields that have a // doc: comment.

func extractStructs(f *ast.File) map[string][]fieldInfo {

	result := make(map[string][]fieldInfo)



	for _, decl := range f.Decls {

		genDecl, ok := decl.(*ast.GenDecl)

		if !ok || genDecl.Tok != token.TYPE {

			continue

		}

		for _, spec := range genDecl.Specs {

			typeSpec, ok := spec.(*ast.TypeSpec)

			if !ok {

				continue

			}

			structType, ok := typeSpec.Type.(*ast.StructType)

			if !ok {

				continue

			}

			structName := typeSpec.Name.Name

			var fields []fieldInfo

			for _, field := range structType.Fields.List {

				if len(field.Names) == 0 {

					continue

				}

				doc := extractDocComment(field.Comment)

				if doc == "" {

					doc = extractDocComment(field.Doc)

				}

				if doc == "" {

					continue

				}



				fieldName := field.Names[0].Name

				yamlKey := yamlTagKey(field.Tag, fieldName)

				if yamlKey == "-" {

					continue

				}



				goTyp := simplifyType(field.Type)

				def := defaults[structName+"."+fieldName]
				if def == "" {
					def = extractDocDefault(doc)
				}



				fields = append(fields, fieldInfo{

					yamlKey: yamlKey,

					goType:  goTyp,

					doc:     doc,

					def:     def,

				})

			}

			if len(fields) > 0 {

				result[structName] = fields

			}

		}

	}

	return result

}



// extractDocComment extracts text after "// doc: " from a comment group.

func extractDocComment(cg *ast.CommentGroup) string {

	if cg == nil {

		return ""

	}

	for _, c := range cg.List {

		text := c.Text

		const prefix = "// doc: "

		if idx := strings.Index(text, prefix); idx >= 0 {

			return strings.TrimSpace(text[idx+len(prefix):])

		}

	}

	return ""

}



// yamlTagKey extracts the yaml key from a struct tag, or falls back to

// converting the field name to snake_case.

func yamlTagKey(tag *ast.BasicLit, fieldName string) string {

	if tag != nil {

		raw := tag.Value

		// strip surrounding backticks

		raw = strings.Trim(raw, "`")

		st := reflect.StructTag(raw)

		val := st.Get("yaml")

		if val != "" {

			key := strings.Split(val, ",")[0]

			if key == "-" {

				return "-"

			}

			if key != "" {

				return key

			}

		}

	}

	return toSnakeCase(fieldName)

}



// toSnakeCase converts CamelCase to snake_case.

func toSnakeCase(s string) string {

	var b strings.Builder

	for i, r := range s {

		if unicode.IsUpper(r) && i > 0 {

			b.WriteByte('_')

		}

		b.WriteRune(unicode.ToLower(r))

	}

	return b.String()

}



// simplifyType converts ast.Expr to a human-readable type string.

func simplifyType(expr ast.Expr) string {

	switch t := expr.(type) {

	case *ast.Ident:

		switch t.Name {

		case "string":

			return "string"

		case "int":

			return "int"

		case "bool":

			return "bool"

		case "StringOrList":

			return "string or []string"

		case "PostAction":

			return "string or object"

		case "WhenExpr":

			return "string or object"

		default:

			return t.Name

		}

	case *ast.ArrayType:

		inner := simplifyType(t.Elt)

		switch inner {

		case "CueRef":

			return "[]cue"

		default:

			return "[]" + inner

		}

	case *ast.MapType:

		return "map"

	case *ast.StarExpr:

		return simplifyType(t.X)

	default:

		return fmt.Sprintf("%T", expr)

	}

}



// dashOrBacktick returns "—" for empty defaults, or the value wrapped in backticks.

func dashOrBacktick(def string) string {

	if def == "" {

		return "—"

	}

	return "`" + def + "`"

}

