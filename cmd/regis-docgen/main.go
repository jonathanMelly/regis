const sectionKeyConceptsBody = `

### requires / needs



` + "`requires: [scenario-a, scenario-b]`" + ` declares prerequisites — scenarios that must complete before this one starts. Prerequisites are **deduplicated**: if multiple scenarios all require ` + "`build`" + `, it runs exactly once. ` + "`needs:`" + ` is an accepted alias.

Each scenario in ` + "`requires:`" + ` is **independent**: it runs as its own unit with its own ` + "`on_error`" + ` policy. A failure in a prerequisite does **not** trigger the dependent scenario's ` + "`on_error: compensate`" + `. Use inline scenario refs (` + "`{ scenario: x }`" + ` in ` + "`cues:`" + `) when you need the parent's ` + "`on_error`" + ` to cover the referenced scenario's cues.



### Post-action deduplication



Post-actions from all changed cues are collected, deduplicated by command string, then executed once. Five cues all declaring ` + "`post: reload:nginx`" + ` result in exactly one nginx reload. Same command + different ` + "`sudo:`" + ` values → ` + "`sudo: true`" + ` wins.



### Scenario-level post



A ` + "`post:`" + ` field on a scenario fires once if **any remote cue** in the scenario changed — regardless of which ones. Local cues (` + "`local: true`" + `) never trigger it.



` + "```yaml\nSaver:\n  post: restart:saver   # runs once if env or bin changed; skipped if only local build changed\n  cues:\n    - { name: build, nature: action, local: true, cmd: go build … }\n    - { name: env,   nature: secret, … }   # changing this triggers the post\n    - name: saver             # managed_by: registers the service — restart:saver resolves here\n      src: bin/saver\n      dest: saver\n      managed_by: crontab\n      # OR: managed_by: {manager: systemd, service_file: deploy/saver.service, sudo: true}\n```" + `



Scenario-level post and cue-level posts are pooled together before deduplication, so identical commands across both levels still run exactly once.



### Service shorthands

` + "```yaml\npost: restart:saver   # expands to the service's restart command\npost: reload:nginx    # expands to the service's reload command\n```" + `

` + "Shorthands scan all scenarios for a service cue (" + "`nature: service`" + " or a binary cue with `managed_by:`) whose `name:` matches, call " + "`manager.ExpandCommands`" + ", and inherit the service cue's `sudo:` flag. Expanded before deduplication. `deploy:<name>` is also supported (runs `systemctl daemon-reload && systemctl enable <name>` for systemd)." + `

### Service command overrides

` + "`commands:`" + ` overrides or adds actions for any service. Template variables: ` + "`{name}`" + `, ` + "`{binary}`" + `, ` + "`{dir}`" + `, ` + "`{service_file}`" + `. Action references ` + "`{start}`" + `, ` + "`{stop}`" + `, ` + "`{restart}`" + `, ` + "`{reload}`" + ` etc. expand to the **pre-override** base command, enabling a call-super pattern:

` + "```yaml\nMyScenario:\n  cues:\n    - name: nginx-front   # infers nature: service from manager:\n      manager: systemd\n      sudo: true\n      commands:\n        reload: nginx -t && {reload}   # test config, then run original systemctl reload nginx-front\n```" + `

### managed_by: binary + service in one cue

` + "`managed_by:`" + ` is the preferred approach when you deploy a **custom binary** that also needs service management. It combines binary upload and service registration into a single cue — no separate `+ "`nature: service`" + ` cue required.

` + "```yaml\nDeploy:\n  post: restart:app      # resolved via the managed_by: cue below\n  cues:\n    - name: app\n      src: bin/app\n      dest: app\n      managed_by: crontab   # scalar — nature: binary inferred from src: + managed_by:\n\n    - name: app\n      src: bin/app\n      dest: app\n      managed_by:            # struct — systemd with unit file\n        manager: systemd\n        service_file: deploy/app.service\n        sudo: true\n        restart: false        # skip auto-restart; let scenario post: handle it\n```" + `

` + "The `restart: true` default restarts the service immediately after upload when the binary changed. Set `restart: false` when the scenario `post:` handles the restart (e.g. to ensure config is also applied before restarting)." + `

### requires vs inline scenario refs

Two mechanisms for scenario composition — choose consciously:

| | ` + "`requires:`" + ` | inline ` + "`{ scenario: x }`" + ` in ` + "`cues:`" + ` |
|---|---|---|
| Deduplication | yes — each scenario runs once even if required by many | no — expands at the call-site position each time |
| Ordering | all prerequisites complete before parent starts | cues interleave at the exact position in the list |
| ` + "`on_error`" + ` scope | each scenario is independent — parent's compensate does not cover it | parent's ` + "`on_error`" + ` applies — a failure in an inlined cue triggers the composing scenario's compensation |
| Use when | "these must finish first; they own their error handling" | "these cues are part of me; restore everything together" |

` + "```yaml\n# requires: — files deploy independently, before deploy starts.\n# If app fails, app's on_error applies (default: halt). deploy's compensate never fires.\ndeploy:\n  requires: [app, config]\n  on_error: compensate   # only covers deploy's own cues\n  cues:\n    - name: migrate ...\n\n# inline ref — files are part of deploy. If app's pack cue fails,\n# deploy's on_error: compensate fires for everything.\ndeploy:\n  on_error: compensate\n  cues:\n    - { scenario: app }    # expanded inline; parent's on_error covers this\n    - { scenario: config }\n    - name: migrate ...\n```" + `

Narrowing: ` + "`{ scenario: app, cue: source }`" + ` runs only the named cue; ` + "`{ scenario: app, cues: [source, env] }`" + ` runs a subset. Nested refs are supported.`

