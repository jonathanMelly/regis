// generate writes the AI context document to outFile, sourcing data from the

// provided paths. All paths may be absolute or relative to the process cwd.

func generate(typesFile, cueDir, interpolateFile, cmdDir, exampleFile, outFile string) error {

	// 1. Parse types.go for schema tables

	schemaSection, err := buildSchemaSection(typesFile)

	if err != nil {

		return err

	}



	// 2. Parse cue files for nature types

	natureSection := buildNatureSection(cueDir)



	// 3. Parse interpolate.go for interpolation doc

	interpolSection, err := buildInterpolSection(interpolateFile)

	if err != nil {

		return err

	}



	// 4. Parse cobra commands for CLI reference

	cliSection := buildCLISection(cmdDir)



	// 5. Read the example YAML from the given path

	exampleData, err := os.ReadFile(exampleFile)

	if err != nil {

		return fmt.Errorf("regis-docgen: read %s: %w", exampleFile, err)

	}



	// 6. Assemble the full output

	var out bytes.Buffer

	out.WriteString(docgenHeader)



	out.WriteString("## Schema Reference\n\n")

	out.WriteString(schemaSection)



	out.WriteString("## Nature Types\n\n")

	out.WriteString(natureSection)



	out.WriteString("## Interpolation\n\n")

	out.WriteString(interpolSection)



	out.WriteString("## Key Concepts\n")

	out.WriteString(sectionKeyConceptsBody)

	out.WriteString("\n\n---\n\n")

	out.WriteString("## Mixed regis + manual deployments\n")

	out.WriteString(sectionMixedDeploys)

	out.WriteString("\n\n---\n\n")

	out.WriteString("## CLI Reference\n\n")

	out.WriteString(cliSection)



	out.WriteString("## Example regis.yml\n\n")

	out.WriteString("```yaml\n")

	out.Write(exampleData)

	out.WriteString("```\n")



	if err := os.WriteFile(outFile, out.Bytes(), 0644); err != nil {

		return fmt.Errorf("regis-docgen: write %s: %w", outFile, err)

	}



	fmt.Printf("regis-docgen: wrote %s\n", outFile)

	return nil

}



// generateSchema writes a dense annotated YAML schema to outFile.
func generateSchema(typesFile, outFile string) error {
	content, err := buildDenseSchema(typesFile)
	if err != nil {
		return err
	}
	if err := os.WriteFile(outFile, []byte(content), 0644); err != nil {
		return fmt.Errorf("regis-docgen: write %s: %w", outFile, err)
	}
	fmt.Printf("regis-docgen: wrote %s\n", outFile)
	return nil
}

// buildDenseSchema generates a dense annotated YAML schema reference.
// Values are real/default values; options and mandatory appear only in comments.
func buildDenseSchema(typesFile string) (string, error) {
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, typesFile, nil, parser.ParseComments)
	if err != nil {
		return "", fmt.Errorf("regis-docgen: parse %s: %w", typesFile, err)
	}
	s := extractStructs(f)

	var b bytes.Buffer

	// fl writes one aligned YAML field line with an inline comment.
	fl := func(indent, key, val, comment string) {
		kv := indent + key + ": " + val
		if comment != "" {
			fmt.Fprintf(&b, "%-44s  # %s\n", kv, comment)
		} else {
			fmt.Fprintf(&b, "%s\n", kv)
		}
	}

	b.WriteString("# regis.yml schema — auto-generated by regis-docgen, do not edit\n")
	b.WriteString("# mandatory fields are marked in comments\n\n")

	// targets
	b.WriteString("targets:\n")
	reqTarget := map[string]bool{"name": true, "host": true}
	for i, fi := range s["Target"] {
		pfx := "    "
		if i == 0 {
			pfx = "  - "
		}
		fl(pfx, fi.yamlKey, schemaVal(fi, reqTarget[fi.yamlKey]), schemaComment(fi, reqTarget[fi.yamlKey]))
	}
	b.WriteString("\n")

	// defaults — show env as an example key-value block
	b.WriteString("defaults:\n")
	for _, fi := range s["DefaultsConfig"] {
		if fi.yamlKey == "env" {
			fmt.Fprintf(&b, "%-44s  # %s\n", "  env:", schemaComment(fi, false))
			b.WriteString("    APP_ENV: production\n")
			b.WriteString("    LOG_LEVEL: info\n")
		} else {
			fl("  ", fi.yamlKey, schemaVal(fi, false), schemaComment(fi, false))
		}
	}
	b.WriteString("\n")

	// scenarios
	b.WriteString("scenarios:\n")
	b.WriteString("  <name>:\n")
	for _, fi := range s["Scenario"] {
		switch fi.yamlKey {
		case "cues", "checks", "compensate":
			continue // expanded below
		}
		fl("    ", fi.yamlKey, schemaVal(fi, false), schemaComment(fi, false))
	}
	// cues list
	b.WriteString("    cues:\n")
	cueReq := map[string]bool{"name": true, "nature": true}
	for i, fi := range s["CueRef"] {
		pfx := "        "
		if i == 0 {
			pfx = "      - "
		}
		switch fi.yamlKey {
		case "src":
			b.WriteString("        # ── file upload: binary/secret = single file · config = multi-file/glob/tree · pack = glob tree with prune · or use git: true instead of src for pack ──\n")
		case "shell":
			b.WriteString("        # ── action / generate ──\n")
		case "post":
			b.WriteString("        # ── common remote cue fields (binary · config · secret · pack · service · render) ──\n")
	case "diff_mode":
			b.WriteString("        # ── pack / config / render: diff control ──\n")
		case "prune":
			b.WriteString("        # ── pack / render folder mode ──\n")
		case "manager":
			b.WriteString("        # ── service (manager: present infers nature: service) ──\n")
		case "managed_by":
			b.WriteString("        # ── managed_by: binary + service in one cue (preferred for custom binaries) ──\n")
		case "compensation":
			b.WriteString("        # ── per-cue compensation (infers on_error: compensate; \"cmd\"/{shell,sudo}/defer/interactive) ──\n")
		}
		fl(pfx, fi.yamlKey, schemaVal(fi, cueReq[fi.yamlKey]), schemaComment(fi, cueReq[fi.yamlKey]))
	}
	// checks list — same structure as cues but read-only probes
	b.WriteString("    checks:                  # terminal acceptance probes; run after cues complete\n")
	b.WriteString("      - name: <string>       # required\n")
	b.WriteString("        nature: action       # checks are read-only action cues\n")
	b.WriteString("        shell: <string>      # probe command\n")
	// compensate list — scenario-level compensation actions
	b.WriteString("    compensate:              # scenario-level actions run when on_error: compensate triggers\n")
	b.WriteString("      - name: <string>       # required\n")
	b.WriteString("        shell: <string>      # remote command; $STATE_ID is available\n")
	b.WriteString("        sudo: false\n")
	b.WriteString("\n")

	// run
	b.WriteString("run:\n")
	for _, fi := range s["RunConfig"] {
		fl("  ", fi.yamlKey, schemaVal(fi, false), schemaComment(fi, false))
	}
	b.WriteString("\n")

	// state — show dir with a concrete example path
	b.WriteString("state:\n")
	for _, fi := range s["StateConfig"] {
		val := schemaVal(fi, false)
		if fi.yamlKey == "dir" {
			val = "/opt/app/.regis-states" // representative example
		}
		fl("  ", fi.yamlKey, val, schemaComment(fi, false))
	}
	b.WriteString("\n")

	// concurrency
	if len(s["ConcurrencyConfig"]) > 0 {
		b.WriteString("concurrency:\n")
		for _, fi := range s["ConcurrencyConfig"] {
			fl("  ", fi.yamlKey, schemaVal(fi, false), schemaComment(fi, false))
		}
		b.WriteString("\n")
	}

	// pre / post: run before/after all cues; local: true = local machine, default = SSH target
	b.WriteString("pre:\n")
	b.WriteString(fmt.Sprintf("%-44s  # runs on target via SSH before any cue\n", "  - cmd: \"systemctl stop watchdog\""))
	b.WriteString(fmt.Sprintf("%-44s  # runs on local machine (notify, build, etc.)\n", "  - cmd: \"curl -s https://hooks/deploy-start\""))
	b.WriteString(fmt.Sprintf("%-44s\n", "    local: true"))
	b.WriteString("\n")
	b.WriteString("post:\n")
	b.WriteString(fmt.Sprintf("%-44s  # runs on target via SSH after all cues succeed\n", "  - cmd: \"systemctl start watchdog\""))
	b.WriteString(fmt.Sprintf("%-44s  # runs on local machine after deploy\n", "  - cmd: \"curl -s https://hooks/deploy-done\""))
	b.WriteString(fmt.Sprintf("%-44s\n", "    local: true"))
	b.WriteString("\n")

	return b.String(), nil
}

// schemaVal returns the actual/default YAML value for a field.
// mandatory fields with alternatives show the first alternative as an example value.
func schemaVal(fi fieldInfo, mandatory bool) string {
	alts := schemaExtractAlts(fi.doc)
	def := fi.def

	if mandatory {
		// Show first alternative as example (e.g. "binary" for nature), else empty
		if len(alts) > 0 {
			return alts[0]
		}
		return `""`
	}

	switch fi.goType {
	case "bool":
		if def == "true" {
			return "true"
		}
		return "false"
	case "int":
		if def != "" {
			return def
		}
		return "0"
	case "map":
		return "{}"
	case "string or []string":
		return "[]"
	case "string or object":
		return `""`
	case "[]cue":
		return "[]"
	default: // string
		if def != "" {
			return def
		}
		if len(alts) > 0 {
			return alts[0] // first option as example
		}
		return `""`
	}
}

// schemaComment builds the inline comment: "mandatory — opts — short doc"
// or "opts — short doc" or just short doc.
func schemaComment(fi fieldInfo, mandatory bool) string {
	alts := schemaExtractAlts(fi.doc)
	short := schemaShortDoc(fi.doc)

	var parts []string
	if mandatory {
		parts = append(parts, "mandatory")
	}
	if len(alts) > 0 {
		parts = append(parts, strings.Join(alts, "|"))
	}
	if short != "" {
		parts = append(parts, short)
	}
	return strings.Join(parts, " — ")
}

// extractDocDefault pulls the value from a "(default: X)" or "(default X)" annotation.
func extractDocDefault(doc string) string {
	i := strings.Index(doc, "(default")
	if i < 0 {
		return ""
	}
	rest := strings.TrimLeft(doc[i+len("(default"):], ": ")
	j := strings.Index(rest, ")")
	if j < 0 {
		return ""
	}
	return strings.TrimSpace(rest[:j])
}

// schemaShortDoc strips alternatives and default hints from a doc string,
// leaving only the human description.
func schemaShortDoc(doc string) string {
	// Strip " (default: x)" or " (default x)" suffix
	if i := strings.Index(doc, " (default"); i >= 0 {
		doc = doc[:i]
	}
	// "description: opt1 | opt2" → strip ": opt1 | opt2"
	if i := strings.LastIndex(doc, ": "); i >= 0 {
		if strings.Contains(doc[i+2:], " | ") {
			doc = strings.TrimSpace(doc[:i])
		}
	}
	// "opt1 | opt2 — description" → take text after " — "
	if i := strings.Index(doc, " \u2014 "); i >= 0 {
		if strings.Contains(doc[:i], " | ") {
			doc = strings.TrimSpace(doc[i+4:])
		}
	}
	return doc
}

// schemaExtractAlts parses "opt1 | opt2 | opt3" patterns from a doc string.
// Returns nil when no clean alternatives list is found.
func schemaExtractAlts(doc string) []string {
	if !strings.Contains(doc, " | ") {
		return nil
	}
	s := doc
	// Strip "(default: x)" suffix
	if i := strings.Index(s, " (default:"); i >= 0 {
		s = s[:i]
	}
	// Strip " — description" suffix (em dash: U+2014); alternatives are before it
	if i := strings.Index(s, " \u2014 "); i >= 0 {
		s = s[:i]
	}
	// If pattern is "description: opt1 | opt2", take only the opts part
	if i := strings.LastIndex(s, ": "); i >= 0 {
		candidate := s[i+2:]
		if strings.Contains(candidate, " | ") {
			s = candidate
		}
	}
	parts := strings.Split(s, " | ")
	var result []string
	for _, p := range parts {
		p = strings.TrimSpace(p)
		// Skip parts that look like prose (contain punctuation or are long)
		if p == "" || len(p) > 20 || strings.ContainsAny(p, ".,;()") {
			return nil
		}
		result = append(result, p)
	}
	if len(result) < 2 {
		return nil
	}
	return result
}

